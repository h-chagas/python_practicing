#CONCATENATION AND REPETITION (with Arithmetic operators)

#Concatenation
single_char1 = "K"
single_char2 = "F"
single_char3 = "C"
print(single_char1 + single_char2 + single_char3)

#Repetition
restaurant_name = single_char1 + single_char2 + single_char3
restaurant_name_10_times = restaurant_name * 10
print(restaurant_name_10_times)