# Arithmetic operators

# Add
print("add", 2 + 5)
print("add", 2.2 + 5)

#Subtraction
print("subtraction", 5 - 2)

#Multiplication
print("multiplication", 2 * 10)

#Division
print("Division", 10 / 2) #always float number

#Division (result in int)
print("Division", 10 // 2) #always int number

#Exponential
print("exponential", 2 ** 10)

#Module
print("module", 10 % 3) #rest of the division


####### Precedence
# 1. parenthesis ()
# 2. exponential **
# 3. multiplication, division and module *, /, // and %
# 4. addition and subtraction + and -

